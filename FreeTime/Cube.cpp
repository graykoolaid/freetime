Cube::Cube()
{
	this->indice_len = 36;
	this->vertice_len = 8;
	indices = new unsigned short[36];
	vertices = new VertexPositionColor[8];

	ifstream file("cube.txt");
	if (file.is_open())
	{
		for(int i = 0; i < 5; i++)
		{
			file >> indices[i];
		}
	}

	vertices[0] = { XMFLOAT3(-0.5f, -0.5f, -0.5f), XMFLOAT3(0.0f, 0.0f, 0.0f) };
	vertices[1] = { XMFLOAT3(-0.5f, -0.5f,  0.5f), XMFLOAT3(0.0f, 0.0f, 1.0f) };
	vertices[2] = { XMFLOAT3(-0.5f,  0.5f, -0.5f), XMFLOAT3(0.0f, 1.0f, 0.0f) };
	vertices[3] = { XMFLOAT3(-0.5f,  0.5f,  0.5f), XMFLOAT3(0.0f, 1.0f, 1.0f) };
	vertices[4] = { XMFLOAT3(0.5f, -0.5f, -0.5f), XMFLOAT3(1.0f, 0.0f, 0.0f) };
	vertices[5] = { XMFLOAT3(0.5f, -0.5f,  0.5f), XMFLOAT3(1.0f, 0.0f, 1.0f) };
	vertices[6] = { XMFLOAT3(0.5f,  0.5f, -0.5f), XMFLOAT3(1.0f, 1.0f, 0.0f) };
	vertices[7] = { XMFLOAT3(0.5f,  0.5f,  0.5f), XMFLOAT3(1.0f, 1.0f, 1.0f) };

	return;
}

